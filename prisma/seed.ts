import { PrismaClient } from '@prisma/client';

const prisma = new PrismaClient();

async function main() {
  const admin = await prisma.user.upsert({
    where: { email: 'admin@soccer.com' },
    update: {},
    create: {
      email: 'admin@soccer.com',
      password: 'password123', // TODO: Replace with bcrypt
      name: 'Admin de Prueba',
      role: 'ADMIN',
    },
  });

  console.log({ admin });
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
